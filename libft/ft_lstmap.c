#include "libft.h"

t_list	*ft_lstmap(t_list *lst, void *(*f)(void *), void (*del)(void *))
{
	t_list	*complete;
	t_list	*node;

	if (!f || !lst)
		return (NULL);
	complete = NULL;
	while (lst)
	{
		node = ft_lstnew(f(lst->content));
		if (!node)
		{
			ft_lstclear(&node, (*del));
			return (NULL);
		}
		ft_lstadd_back(&complete, node);
		lst = lst->next;
	}
	return (complete);
}
